	<div class="card-text p-4">
		<p style="text-align: justify;">Ekonomi kreatif berasal dari inovasi dan kreativitas yang dimiliki oleh masyarakat, yang digunakan untuk mengembangkan keterampilan sebagai aset utama dalam menciptakan barang dan jasa yang memiliki nilai ekonomi. Keterampilan masyarakat didukung dengan ciri khas budaya, keberlanjutan sumber daya alam, dan bahkan faktor geografis yang menjadi identitas negara atau tempat tinggal masyarakat tersebut. Ekonomi kreatif tidak hanya berfokus pada peningkatan nilai tambah secara ekonomi, tetapi juga pada penciptaan nilai tambah dalam aspek sosial budaya dan lingkungan. Karenanya, ekonomi kreatif memiliki potensi untuk meningkatkan daya saing tidak hanya dalam hal ekonomi, tetapi juga dalam hal-hal lainnya.
		</p>
		<p style="text-align: justify;">Dalam rangka upaya pembaharuan data mengenai pelaku ekonomi kreatif SubSektor Kriya Di Kota Bandung, maka dari itu perlu dilakukan sebuah pendataan dan pemetaan terhadap pelaku ekonomi kreatif subsektor Kriya di Kota Bandung. Pendataan pelaku ekonomi kreatif bertujuan untuk pertumbuhan usaha binaan agar dinamis, oleh karenanya agar binaan dapat difasilitasi program dan kegiatan yang akan dijalankan perlu dilakukan updating data secara berkala. Pendaftaran untuk menjadi binaan dapat dilakukan dengan melakukan proses pendaftaran pada dinas atau instansi pemerintah seperti Dinas Kebudayaan dan Pariwisata Kota Bandung, dan Dewan Kerajinan Nasional Daerah Kota Bandung.
